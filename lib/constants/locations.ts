// Function to search locations
export function searchLocations(query: string, limit: number = 10): string[] {
  if (!query || query.length < 2) return [];

  const lowerQuery = query.toLowerCase();

  return locations
    .filter((location) => location.toLowerCase().includes(lowerQuery))
    .slice(0, limit);
}
